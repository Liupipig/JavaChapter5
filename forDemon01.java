package com.itheima.branch;

public class forDemon01 {
    public static void main(String[] args) {
        //掌握for循环的书写格式
        for (int i = 0; i < 5; i++){
            System.out.println("hello,world");
        }
        System.out.println("--------------------------");
        for (int i = 1; i <= 5; i++){
            System.out.println("hello,world2");
        }
        System.out.println("--------------------------");
        for (int i = 1; i <= 10; i += 2){
            System.out.println("hello,world3");
        }
    }
}
